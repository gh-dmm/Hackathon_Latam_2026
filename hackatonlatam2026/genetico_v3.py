T = 27
R_obs, Delta_S_obs, S_inicial = preparar_ventana_semanal(
    df_lib, df_cambio, df_total, df_evap, df_batimetria, fecha_inicio_str,T
)

S_max = 3387000.0          # TCM
S_min = 0.25 * S_max       # Almacenamiento crítico (25%)
mediana_R = np.median(R_obs)
delta_u = 0.25 * mediana_R # Ajuste base
u_max = 2 * delta_u

# Niveles discretos permitidos L=5
NIVELES_PERMITIDOS = [-2*delta_u, -delta_u, 0.0, delta_u, 2*delta_u]

# =========================================================
