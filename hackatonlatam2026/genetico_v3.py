# 5. FUNCIÓN FITNESS (Storage Resilience Score - SRS)
# =========================================================
def evaluar_srs(individuo):
    cromosoma_u = np.array(individuo)
    S_opt = np.zeros(T + 1)
    S_opt[0] = S_inicial

    # RESTRICCIÓN DURA: Balance de liberación acumulada (Límite 10%)
    if abs(np.sum(cromosoma_u)) > 0.10 * np.sum(R_obs):
        return (float('1e10'),)

    for t in range(T):
        
       
        # Transición de estado
        S_opt[t+1] = S_opt[t] + Delta_S_obs[t] - cromosoma_u[t]#-Evap_TCM[t]

        # RESTRICCIÓN FÍSICA: No se puede liberar volumen negativo
        if R_obs[t] + cromosoma_u[t] < 0:
            return (float('1e10'),)

        # RESTRICCIÓN FÍSICA: Capacidad de la presa
        if S_opt[t+1] < 0 or S_opt[t+1] > S_max:
            return (float('1e10'),)

    # Penalizaciones (Soft Constraints)
    C_crit = np.sum([max(0, S_min - S_opt[t])**2 for t in range(T+1)])
    C_dev = np.sum(cromosoma_u**2)
    C_smooth = np.sum(np.diff(cromosoma_u)**2)

    # Ponderaciones
    w1 = 1 / ((T + 1) * (S_min**2))
    #w2 = 0.000001 / (T * (u_max**2))
    w2 = 0.1 / (T * (u_max**2))
    w3 = 0.1 / ((T - 1) * ((2 * u_max)**2))

    SRS = (w1 * C_crit) + (w2 * C_dev) + (w3 * C_smooth)
    return (SRS,)

toolbox.register("evaluate", evaluar_srs)

