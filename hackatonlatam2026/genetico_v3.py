def correr_modelo():
    pop = toolbox.population(n=500)

    # INYECCIÓN DEL ÓPTIMO CONOCIDO (Seeding)
    # Crea un individuo con puros ceros para garantizar una línea base factible
    individuo_base = creator.Individual([0.0] * T)
    pop[0] = individuo_base

    hof = tools.HallOfFame(1)
    stats = tools.Statistics(lambda ind: ind.fitness.values)
    stats.register("min", np.min)
    stats.register("avg", np.mean)

    print("Iniciando evolución...")
    pop, log = algorithms.eaSimple(pop, toolbox, cxpb=0.7, mutpb=0.2, ngen=150,
                                   stats=stats, halloffame=hof, verbose=True)

    mejor_solucion = hof[0]

    print("\n" + "="*50)
    print(" RESULTADOS DE LA OPTIMIZACIÓN (DEAP)")
    print("="*50)

    score_final = mejor_solucion.fitness.values[0]
    if score_final >= 1e9:
        print("ADVERTENCIA: El modelo no pudo mejorar la línea base y sigue violando restricciones.")
    else:
        print(f"Mejor Score (SRS): {score_final:.4f}")

    print(f"Desviación de Volumen Total (Límite 10%): {abs(np.sum(mejor_solucion)) / np.sum(R_obs) * 100:.2f}%\n")

    print("Secuencia Óptima de Ajustes u(t):")
    # Imprimir vector de forma legible
    print(np.array(mejor_solucion).round(2))

    return pop, log, hof, mejor_solucion

