def auditar_restricciones(secuencia_u, R_obs, Delta_S_obs, S_inicial, S_max, niveles_permitidos):
    """
    Somete la solución final a una auditoría estricta contra las restricciones del benchmark.
    """
    print("\n" + "═"*55)
    print(" 🔎 AUDITORÍA DE RESTRICCIONES (COMPLIANCE CHECK)")
    print("═"*55)

    T = len(secuencia_u)
    S_opt = np.zeros(T + 1)
    S_opt[0] = S_inicial
    fallos = 0

    # 1. Chequeo de Niveles Discretos (Diseño por construcción)
    # Verifica que el modelo cuántico/genético no haya inventado valores intermedios
    niveles_validos = all(np.isclose(u, niveles_permitidos, atol=1e-5).any() for u in secuencia_u)
    print(f"[1] Niveles de decisión discretos válidos : {'✅ PASÓ' if niveles_validos else '❌ FALLÓ'}")
    if not niveles_validos: fallos += 1

    # 2. Chequeo de Balance Acumulado (Restricción Global del 10%)
    desviacion_total = abs(np.sum(secuencia_u))
    volumen_historico = np.sum(R_obs)
    limite_10_porciento = 0.10 * volumen_historico
    balance_valido = desviacion_total <= limite_10_porciento

    print(f"[2] Balance Acumulado (Límite 10%)        : {'✅ PASÓ' if balance_valido else '❌ FALLÓ'}")
    print(f"    ├─ Desviación propuesta  : {desviacion_total:,.1f} TCM")
    print(f"    └─ Límite máximo (10%)   : {limite_10_porciento:,.1f} TCM")
    if not balance_valido: fallos += 1

    # Variables para rastrear fallos físicos a lo largo del tiempo
    flujo_negativo = False
    desborde = False
    sequia = False

    # 3 y 4. Simulador de Chequeos Físicos paso a paso
    for t in range(T):
        # Flujo de liberación
        flujo_actual = R_obs[t] + secuencia_u[t]
        if flujo_actual < 0:
            flujo_negativo = True

        # Transición del embalse
        S_opt[t+1] = S_opt[t] + Delta_S_obs[t] - secuencia_u[t]
        if S_opt[t+1] > S_max:
            desborde = True
        if S_opt[t+1] < 0:
            sequia = True

    print(f"[3] No-negatividad del flujo (R(t) >= 0)  : {'❌ FALLÓ (Flujo < 0 detectado)' if flujo_negativo else '✅ PASÓ'}")
    if flujo_negativo: fallos += 1

    print(f"[4] Límite inferior del embalse (S >= 0)  : {'❌ FALLÓ (Nivel < 0 detectado)' if sequia else '✅ PASÓ'}")
    if sequia: fallos += 1

    print(f"[5] Límite superior embalse (S <= S_max)  : {'❌ FALLÓ (Desborde detectado)' if desborde else '✅ PASÓ'}")
    if desborde: fallos += 1

    print("-" * 55)
    if fallos == 0:
        print("🚀 DICTAMEN FINAL: La secuencia es 100% FACTIBLE.")
        print("   Cumple con todas las reglas operativas del benchmark.")
    else:
        print(f"⚠️ DICTAMEN FINAL: Solución INVIABLE. Se detectaron {fallos} violaciones.")
    print("═"*55 + "\n")

